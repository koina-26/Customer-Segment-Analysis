# Remove rows where CustomerID is missing
df = df.dropna(subset=['CustomerID'])

# Remove cancelled orders
# Cancelled orders have InvoiceNo starting with 'C'
df = df[~df['InvoiceNo'].astype(str).str.startswith('C')]

# Remove rows where Quantity or UnitPrice is zero or negative
df = df[df['Quantity'] > 0]
df = df[df['UnitPrice'] > 0]

# CustomerID to integer
df['CustomerID'] = df['CustomerID'].astype(int)

# InvoiceDate to datetime format
df['InvoiceDate'] = pd.to_datetime(df['InvoiceDate'])

print("Data cleaned!")
print("Remaining rows:", df.shape[0])
print("Unique customers:", df['CustomerID'].nunique())

df['TotalPrice'] = df['Quantity'] * df['UnitPrice']

print("TotalPrice column created!")
print(df[['InvoiceNo', 'Quantity', 'UnitPrice', 'TotalPrice']].head())

df = df[df['Country'] == 'United Kingdom']

print("Filtered to UK customers only")
print("Remaining rows:", df.shape[0])
print("Unique customers:", df['CustomerID'].nunique())

df['Month'] = df['InvoiceDate'].dt.to_period('M')
monthly_sales = df.groupby('Month')['TotalPrice'].sum()

plt.figure(figsize=(13, 5))
monthly_sales.plot(kind='line', color='steelblue', linewidth=2, marker='o')
plt.title('Monthly Revenue Trend')
