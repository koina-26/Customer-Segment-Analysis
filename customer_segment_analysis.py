plt.ylabel('Total Revenue (£)')
plt.xticks(rotation=45)
plt.grid(True, alpha=0.3)
plt.tight_layout()
plt.show()

top_products = df.groupby('Description')['Quantity'].sum().sort_values(ascending=False).head(10)

plt.figure(figsize=(11, 5))
top_products.plot(kind='barh', color='coral', edgecolor='black')
plt.title('Top 10 Best Selling Products')
plt.xlabel('Total Quantity Sold')
plt.gca().invert_yaxis()
plt.tight_layout()
plt.show()

reference_date = df['InvoiceDate'].max() + pd.Timedelta(days=1)

print("Reference date used:", reference_date)

rfm = df.groupby('CustomerID').agg(
    Recency   = ('InvoiceDate', lambda x: (reference_date - x.max()).days),
    Frequency = ('InvoiceNo',   'nunique'),
    Monetary  = ('TotalPrice',  'sum')
).reset_index()

rfm['Monetary'] = rfm['Monetary'].round(2)

print("\nRFM Table (first 5 rows):")
print(rfm.head())
print("\nRFM Table shape:", rfm.shape)

plt.figure(figsize=(14, 4))

plt.subplot(1, 3, 1)
plt.hist(rfm['Recency'], bins=30, color='steelblue', edgecolor='black')
plt.title('Recency Distribution')
plt.xlabel('Days Since Last Purchase')

plt.subplot(1, 3, 2)
plt.hist(rfm['Frequency'], bins=30, color='coral', edgecolor='black')
plt.title('Frequency Distribution')
plt.xlabel('Number of Orders')

plt.subplot(1, 3, 3)
plt.hist(rfm['Monetary'], bins=30, color='mediumseagreen', edgecolor='black')
plt.title('Monetary Distribution')
plt.xlabel('Total Spend (£)')

plt.tight_layout()
plt.show()

# Recency: fewer days = better = higher score
rfm['R_Score'] = pd.qcut(rfm['Recency'], q=4, labels=[4, 3, 2, 1])

# Frequency: more orders = better = higher score
rfm['F_Score'] = pd.qcut(rfm['Frequency'].rank(method='first'), q=4, labels=[1, 2, 3, 4])

# Monetary: more spend = better = higher score
rfm['M_Score'] = pd.qcut(rfm['Monetary'], q=4, labels=[1, 2, 3, 4])

# Convert to integers
rfm['R_Score'] = rfm['R_Score'].astype(int)
rfm['F_Score'] = rfm['F_Score'].astype(int)
rfm['M_Score'] = rfm['M_Score'].astype(int)


rfm['RFM_Score'] = rfm['R_Score'] + rfm['F_Score'] + rfm['M_Score']

print("RFM Scores assigned!")
print(rfm[['CustomerID', 'R_Score', 'F_Score', 'M_Score', 'RFM_Score']].head(10))

def rfm_label(score):
    if score >= 10:
        return 'Champions'
    elif score >= 8:
        return 'Loyal Customers'
    elif score >= 6:
        return 'At Risk'
    elif score >= 4:
        return 'Needs Attention'
    else:
        return 'Lost Customers'

rfm['RFM_Segment'] = rfm['RFM_Score'].apply(rfm_label)

print("RFM Segments:")
print(rfm['RFM_Segment'].value_counts())

plt.figure(figsize=(9, 5))
rfm_counts = rfm['RFM_Segment'].value_counts()

colors_rfm = ['#2ecc71', '#3498db', '#f39c12', '#e67e22', '#e74c3c']
rfm_counts.plot(kind='bar', color=colors_rfm, edgecolor='black')

plt.title('Number of Customers per RFM Segment')
plt.xlabel('RFM Segment')
plt.ylabel('Number of Customers')
plt.xticks(rotation=30)
plt.tight_layout()
plt.show()

from sklearn.preprocessing import StandardScaler

X = rfm[['Recency', 'Frequency', 'Monetary']]


scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)

print("Data scaled and ready for clustering!")
print("Shape:", X_scaled.shape)

inertia = []

for k in range(1, 11):
    km = KMeans(n_clusters=k, random_state=42)
    km.fit(X_scaled)
    inertia.append(km.inertia_)

plt.figure(figsize=(9, 5))
plt.plot(range(1, 11), inertia, 'bo-', linewidth=2, markersize=8)
plt.xlabel('Number of Clusters (K)')
plt.ylabel('Inertia')
plt.title('Elbow Method — Find Best K')
plt.grid(True, alpha=0.3)
plt.show()

kmeans = KMeans(n_clusters=4, random_state=42)
rfm['Cluster'] = kmeans.fit_predict(X_scaled)

print("Clustering done!")
print("\nCustomers per cluster:")
print(rfm['Cluster'].value_counts().sort_index())

colors = ['#e74c3c', '#3498db', '#2ecc71', '#f39c12']

plt.figure(figsize=(10, 6))

for i in range(4):
    cluster_data = rfm[rfm['Cluster'] == i]
    plt.scatter(
        cluster_data['Recency'],
        cluster_data['Monetary'],
        label=f'Cluster {i}',
        color=colors[i],
        s=60,
        alpha=0.7
    )

plt.xlabel('Recency (Days Since Last Purchase)')
plt.ylabel('Monetary (Total Spend £)')
plt.title('Customer Clusters — Recency vs Monetary')
plt.legend()
plt.grid(True, alpha=0.3)
plt.show()

cluster_summary = rfm.groupby('Cluster').agg(
    Total_Customers = ('CustomerID',  'count'),
    Avg_Recency     = ('Recency',     'mean'),
    Avg_Frequency   = ('Frequency',   'mean'),
    Avg_Monetary    = ('Monetary',    'mean')
).round(1)

print("Cluster Summary:")
print(cluster_summary)

